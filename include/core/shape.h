std::vector<Vertex> cube_vertex_array(){
    std::vector<Vertex> vertices;
    size_t size = sizeof(cube_vertices)/sizeof(float);
    for(int i=0;i<size;i+=5){
        Vertex v = Vertex(
            glm::vec3(cube_vertices[i], cube_vertices[i+1], cube_vertices[i+2]),
            glm::vec3(0.5f),
            glm::vec2(cube_vertices[i+3], cube_vertices[i+4]));
        vertices.push_back(v);
    }
    return vertices;
}
